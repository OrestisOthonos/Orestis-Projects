# List all local user accounts excluding system ones
$users = Get-CimInstance Win32_UserProfile | Where-Object {
    $_.Special -eq $false -and $_.LocalPath -like 'C:\Users\*'
}

# OPTIONAL: View user profiles to delete
$users | Select LocalPath

# Delete user profiles
foreach ($user in $users) {
    try {
        Write-Host "Deleting: $($user.LocalPath)"
        Remove-CimInstance $user
    } catch {
        Write-Host "Failed to delete: $($user.LocalPath) - $_"
    }
}

