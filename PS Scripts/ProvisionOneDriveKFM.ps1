# Makes registry changes to deploy OneDrive Known Folder Move (KFM)
# Should be used on PC's that are not domain joined. Domain joined PC's are managed via GPO and don't need this script.
# Version 01.00

if (-not ([Security.Principal.WindowsPrincipal] [Security.Principal.WindowsIdentity]::GetCurrent()).IsInRole([Security.Principal.WindowsBuiltInRole] "Administrator")) {
    # Relaunch as admin
    $script = $MyInvocation.MyCommand.Definition
    Start-Process powershell -ArgumentList "-ExecutionPolicy Bypass -File `"$script`"" -Verb RunAs
    exit
}
$HKLMregistryPath = 'HKLM:\SOFTWARE\Policies\Microsoft\OneDrive'  #Path to HKLM keys
$DiskSizeregistryPath = 'HKLM:\SOFTWARE\Policies\Microsoft\OneDrive\DiskSpaceCheckThresholdMB'  #Path to max disk size key
$TenantGUID = '56c1058d-26dd-4299-b9d2-d25d76787d5d'

if(!(Test-Path $HKLMregistryPath)){New-Item -Path $HKLMregistryPath -Force}
if(!(Test-Path $DiskSizeregistryPath)){New-Item -Path $DiskSizeregistryPath -Force}

New-ItemProperty -Path $HKLMregistryPath -Name 'SilentAccountConfig' -Value '1' -PropertyType DWORD -Force | Out-Null  #Enable silent account configuration
New-ItemProperty -Path $DiskSizeregistryPath -Name $TenantGUID -Value '102400' -PropertyType DWORD -Force | Out-Null  #Set max OneDrive threshold before prompting
New-ItemProperty -Path $HKLMregistryPath -Name 'KFMSilentOptIn' -Value $TenantGUID -PropertyType String -Force | Out-Null  # Silently move known folders to OneDrive.
New-ItemProperty -Path $HKLMregistryPath -Name 'KFMSilentOptInWithNotification' -Value '1' -PropertyType DWORD -Force | Out-Null  # Notify user after migration.
New-ItemProperty -Path $HKLMregistryPath -Name 'KFMBlockOptOut' -Value '1' -PropertyType DWORD -Force | Out-Null  # Prevent users from redirecting their Windows known folders to their PC
New-ItemProperty -Path $HKLMregistryPath -Name 'AutomaticUploadBandwidthPercentage' -Value '32' -PropertyType DWORD -Force | Out-Null  # Limit the sync client upload rate to a percentage of throughput

Write-Output "Done"
PowerShell -NoExit
